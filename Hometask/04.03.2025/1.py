day_num = int(input('Введите число от 1 до 7  - '))
if day_num == 1:
    print('понеделник')
elif day_num == 2:
    print(" вторник")  
elif day_num == 3:
    print(" среда")   
elif day_num == 4:
    print(" четверг")   

elif day_num == 5:
    print(" пятница")  
elif day_num == 6:
    print(" суббота")  
elif day_num == 7:
    print(" воскресенье")  
else:
    print(" не верное число")
num = int(input ("Введите четырехзначное число: "))
if 1000 <= num <= 9999:
    n1 = num // 1000
    n2 = (num // 100) % 10
    n3 = (num // 10) % 10
    n4 = num % 10

    new_num = n4 * 1000 + n3 * 100 + n2 * 10 + n1
    print ("Перевернутое число:", new_num)
else:
    print ("Ошибка! Введите четырехзначное число")